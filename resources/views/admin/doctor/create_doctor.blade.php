<script type="text/javascript">
    $('#dept_id').change(function () {
        var dept_id = $(this).val();
        $.get("{{action('Admin\DoctorController@sub_dept')}}",{
            dept_id: dept_id},function (result) {
            var show_data = '';
            $.each(result,function (i, value) {
                show_data += '<option value="'+value.id+'">'+value.sub_Dept_Name+'</option>';
            });
            $('#sub_dept').html(show_data);
        });
    });
</script>
